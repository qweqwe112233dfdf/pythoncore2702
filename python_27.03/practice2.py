import turtle



turtle.penup()
turtle.goto(0,0)
turtle.pendown()
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)
turtle.forward(100)
turtle.left(90)

turtle.pencolor('purple')
turtle.penup()
turtle.goto(60,0)
turtle.pendown()
turtle.begin_fill()
turtle.fillcolor('purple')
turtle.forward(40)
turtle.right(120)
turtle.forward(100)
turtle.right(120)
turtle.forward(100)
turtle.end_fill()



turtle.done()